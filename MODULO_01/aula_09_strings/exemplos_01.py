frase = 'Curso em Vídeo Python'
print(frase)

#imprime só o caracter especificado
print('imprime só o caracter especificado 9: ',frase[9])
#até o caracter 13
print('até o caracter 13: ',frase[:13])
#do 13 em diante
print('do 13 em diante: ',frase[13:])
#do 1 ao 14
print('do 1 ao 14: ',frase[1:15])
# do 0 ao 21 de 2 em 2
print('do 0 ao 21 de 2 em 2: ',frase[0:21:2])
# de 2 em 2
print('de 2 em 2: ',frase[::2])

