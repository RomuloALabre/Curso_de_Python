nome = input('Digite um nome: ')
if nome == 'Ana':
    print(f'{nome}, você é a filha do Rômulo!')
elif nome == 'Paula':
    print(f'{nome}, você é esposa do Rômulo!')
elif nome == 'Tina':
    print(f'{nome}, mãe do Rômulo!')
else:
    print(f'{nome}, você deve ser a avó do Rômulo!')
print(f'Tenha um ótimo dia, {nome}!')